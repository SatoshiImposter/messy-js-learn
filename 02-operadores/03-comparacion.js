//relacionales

let a = 10;
console.log(a > 5); // true
console.log(a >= 5); // true
console.log(a < 5); // true
console.log(a <= 5); // true

//igualdad

console.log(a == 10); // true, aparte solo pregunta el valor mas no el tipo de dato
console.log(a =! 10); // distinto
console.log(a =! "10"); // distinto
console.log(a === "10"); // false, pregunta el valor y el tipo de dato
console.log(a !== "10"); // true, pregunta el valor y el tipo de dato
